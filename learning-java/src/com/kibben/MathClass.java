package com.kibben;

public class MathClass {
    public static void main(String[] args) {
        // Math
        // Math.abs() turns a negative number into a positive
        System.out.println(Math.abs(-10));
        // Math.max(7, 9) gives you the max number between the two numbers
        System.out.println(Math.max(6, 10));
        // gives you min number between two numbers
        System.out.println(Math.min(6, 10));
        // gives you the result of number power to another number e.g 5 power 2 is 25
        // adding (int) before give a whole number instead of double
        System.out.println( (int) Math.pow(5, 2));
        // gives you square root of a number
        System.out.println((int) Math.sqrt(25));
        //gives you full number of PI
        System.out.println(Math.PI);

    }
}
